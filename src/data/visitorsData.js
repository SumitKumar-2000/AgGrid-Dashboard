for(let i=1;i<=31;i++){
  const ans = HotelBookings.filter(item => item.arrival_date_day_of_month === i && item.arrival_date_month === 'July').reduce((total,curr) => {
    return total + curr.children + curr.adults + curr.babies
  },0)
  visitorsData.visitorsVisitedJuly.push(ans);
}

for(let i=1;i<=9;i++){
  const ans = HotelBookings.filter(item => item.arrival_date_day_of_month === i && item.arrival_date_month === 'August').reduce((total,curr) => {
    return total + curr.children + curr.adults + curr.babies
  },0)
  visitorsData.visitorsVisitedAugust.push(ans);
}

// countries visited
const set = new Set();
HotelBookings.map(item => set.add(item.country))
for(const item of set){
  visitorsData.countryvisited.push(item)
}

// visitors count per country
for(const element of visitorsData.countryvisited){
  const ans = HotelBookings.filter(item => item.country === element)
  .reduce((sum,curr) => {
    return sum + curr.adults + curr.children + curr.babies
  },0)
  visitorsData.visitorsPerCountry.push(ans)
}

